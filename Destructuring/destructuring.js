// Destructuring

let obj2 = {
    name: "John",
    age: 30
};
let { name, age } = obj2;   // object destructuring
console.log(name); // "John"
console.log(age); // 30 years old   person.age

let arr2 = [1, 2, 3];
let [a, b, c] = arr2;   // array destructuring
console.log(a); // 1
console.log(b); // 2
console.log(c); // 3    (array element)   (array item)  (array value)